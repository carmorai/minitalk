#include "ft_printf.h"

size_t	ft_putstr(char *s)
{
	unsigned int	i;

	i = 0;
	if (!s)
		return (ft_putstr("(null)"));
	while (s[i])
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
